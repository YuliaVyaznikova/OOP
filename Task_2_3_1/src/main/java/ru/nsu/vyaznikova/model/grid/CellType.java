package ru.nsu.vyaznikova.model.grid;

/**
 * Перечисление типов клеток на игровом поле, отвечающее за:
 * - Определение всех возможных состояний клетки (пустая, стена, еда, части змейки)
 * - Обеспечение типобезопасности при работе с клетками
 * - Различение головы и тела змейки для визуализации
 */
public enum CellType {
    EMPTY,
    WALL,
    SNAKE_HEAD,
    SNAKE_BODY,
    FOOD
}
